export function 전체리포트_프롬프트(
  로그: 공격로그입력,
  요약: Record<string, unknown>,
  의도: Record<string, unknown>,
  숙련도: Record<string, unknown>,
  권고: Record<string, unknown>
): string {
  return `당신은 사이버 보안 보고서 작성 전문가입니다. 아래 분석 결과를 종합하여 PDF 리포트용 서술 텍스트를 작성하세요.

## 사건 기본 정보
- 사건 ID: ${로그.사건ID}
- 발생 시각: ${로그.발생시각}
- 공격자 IP: ${로그.공격자IP} (${로그.공격자국가 ?? "국가 불명"})
- 위험 점수: ${로그.위험점수}/100

## 분석 결과 요약
- 사건 요약: ${String(요약["요약문"] ?? "")}
- 공격 의도: ${String(의도["의도"] ?? "")} (신뢰도: ${(Number(의도["신뢰도"] ?? 0) * 100).toFixed(0)}%)
- 숙련도: ${String(숙련도["등급"] ?? "")}
- 대응 우선순위: ${String(권고["대응우선순위"] ?? "")}

## 출력 형식 (반드시 아래 JSON만 출력, 다른 텍스트 금지)
{
  "리포트서술": "4~8문장의 전문적이고 이해하기 쉬운 한국어 서술. 공격 개요, 공격자 특성, 위험성, 권고 조치를 포함할 것."
}`;
}

