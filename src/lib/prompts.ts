import type { 공격로그입력 } from "@/types/input";

export function 사건요약_프롬프트(로그: 공격로그입력): string {
  return `당신은 사이버 보안 전문가입니다. 아래 공격 로그를 분석하고 비전문가도 이해할 수 있는 한국어 요약을 작성하세요.

## 공격 로그 정보
- 공격 유형: ${로그.공격유형}
- 공격자 IP: ${로그.공격자IP}
- 공격자 국가: ${로그.공격자국가 ?? "알 수 없음"}
- 위험 점수: ${로그.위험점수}/100
- 위험 등급: ${로그.위험등급}
- OWASP 분류: ${로그.OWASP분류 ?? "미분류"}
- MITRE ATT&CK: ${로그.MITRE전술 ?? "미분류"}
- 페이로드: ${(로그.페이로드 ?? "없음").slice(0, 300)}
- 대상 경로: ${로그.대상URI ?? "알 수 없음"}
- 행위 시퀀스: ${로그.행위시퀀스.length > 0 ? 로그.행위시퀀스.join(" → ") : "단일 공격"}
- 총 요청 횟수: ${로그.요청횟수}회

## 출력 형식 (반드시 아래 JSON만 출력, 다른 텍스트 금지)
{
  "요약문": "비전문가도 이해 가능한 1~3문장 한국어 요약",
  "핵심포인트": ["핵심 내용1", "핵심 내용2", "핵심 내용3"],
  "공격명칭": "공격 유형 한국어 명칭 (예: SQL 인젝션 공격)"
}`;
}

