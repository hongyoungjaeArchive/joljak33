export function 숙련도분석_프롬프트(로그: 공격로그입력): string {
  return `당신은 사이버 보안 포렌식 전문가입니다. 공격 패턴과 페이로드를 분석하여 공격자의 기술 숙련도를 판단하세요.

## 공격 로그 정보
- 공격 유형: ${로그.공격유형}
- 페이로드: ${(로그.페이로드 ?? "없음").slice(0, 300)}
- User-Agent: ${로그.사용자에이전트 ?? "알 수 없음"}
- 행위 시퀀스: ${로그.행위시퀀스.length > 0 ? 로그.행위시퀀스.join(" → ") : "단일 공격"}
- 총 요청 횟수: ${로그.요청횟수}회
- HTTP 메서드: ${로그.HTTP메서드 ?? "알 수 없음"}

## 숙련도 기준
- Script Kiddie: 자동화 도구 사용, 단순 패턴, 기본 페이로드
- Intermediate: 수동+자동 혼합, 환경에 맞춘 페이로드, 일부 우회 시도
- Advanced: 맞춤형 페이로드, 복잡한 행위 시퀀스, 탐지 우회 기술

## 출력 형식 (반드시 아래 JSON만 출력, 다른 텍스트 금지)
{
  "등급": "Script Kiddie 또는 Intermediate 또는 Advanced",
  "근거목록": ["근거1", "근거2", "근거3"],
  "종합설명": "종합 판단 2~3문장 한국어"
}`;
}

