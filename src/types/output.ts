// LLM 분석 결과 타입 (models/output.py → TypeScript)

export type 공격의도코드 =
  | "정보수집"
  | "취약점탐색"
  | "침투시도"
  | "데이터탈취"
  | "서비스방해"
  | "불명";

export type 숙련도코드 = "Script Kiddie" | "Intermediate" | "Advanced";

// ─── 역할 1: 사건 요약 ───────────────────────────────────────────────────────
export interface 사건요약결과 {
  요약문: string;
  핵심포인트: string[];
  공격명칭: string;
}

// ─── 역할 2: 공격 의도 분석 ─────────────────────────────────────────────────
export interface 의도분석결과 {
  의도: 공격의도코드;
  신뢰도: number; // 0~1
  판단근거: string;
}

// ─── 역할 3: 숙련도 추론 ─────────────────────────────────────────────────────
export interface 숙련도분석결과 {
  등급: 숙련도코드;
  근거목록: string[];
  종합설명: string;
}

// ─── 역할 4: 대응 권고 ───────────────────────────────────────────────────────
export interface 대응권고결과 {
  즉각조치: string[];
  장기권고: string[];
  대응우선순위: string;
}

// ─── 역할 5: 전체 리포트 종합 ────────────────────────────────────────────────
export interface 전체리포트결과 {
  사건요약: 사건요약결과;
  의도분석: 의도분석결과;
  숙련도분석: 숙련도분석결과;
  대응권고: 대응권고결과;
  리포트서술: string;
  생성시각: string;
  사용모델: string;
}

// ─── 스트리밍 이벤트 타입 ────────────────────────────────────────────────────
export type 스트리밍이벤트 =
  | { 유형: "시작"; 메시지: string }
  | { 유형: "단계시작"; 단계: number; 총단계: number; 이름: string }
  | { 유형: "단계완료"; 단계: number; 이름: string; 결과: Record<string, unknown> }
  | { 유형: "토큰"; 텍스트: string }
  | { 유형: "완료"; 결과: Record<string, unknown>; 분석유형: string }
  | { 유형: "오류"; 메시지: string };
